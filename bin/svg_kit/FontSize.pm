package FontSize;
use strict;
use Data::Dumper;

## creat a new obejct, and set the parameters
sub new
{	
	my $invocant = shift;
	my $class = ref($invocant) || $invocant;
	my $self = {'font'=>undef,@_ } ;
	bless($self, $class);
	$self->initial();
	return $self;
}



sub stringHeight {
	my ($self,$size) = @_;
	return int($size*0.72);
}

sub stringWidth{
	my ($self,$family,$size,$string) = @_;
	$family =~ s/Courier/CourierNew/ if($family eq "Courier" || $family =~ /Courier-/);
	$family =~ s/Times/TimesNewRoman/ if($family eq "Times" || $family =~ /Times-/);
	my $family_p = $self->{font}{$family};
	my @char = split //,$string;
	my $width;
	for (my $i=0; $i<@char; $i++) {
		$width += $family_p->{$char[$i]};
	}
	return int($width*$size*0.00048820179);
}


